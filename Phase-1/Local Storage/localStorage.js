// ****** : Local Storage In Javascript ******


// Local Storage helps to save the data in users computers in the form of a "STRING".

// Every domain has its Local Storage. 

// Syntax to setItem 
localStorage.setItem('name', 'Akash');

// Syntax to show/ print  localStorage 
localStorage;

// Syntax to clear localStorage 
localStorage.clear();

// Syntax to get a particular previously setItem 
localStorage.getItem();

//Syntax to remove a particular item 
localStorage.removeItem('name');
